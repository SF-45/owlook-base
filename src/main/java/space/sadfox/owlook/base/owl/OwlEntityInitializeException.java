package space.sadfox.owlook.base.owl;

public class OwlEntityInitializeException extends Exception {

	public OwlEntityInitializeException() {
		super();
	}

	public OwlEntityInitializeException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public OwlEntityInitializeException(String message, Throwable cause) {
		super(message, cause);
	}

	public OwlEntityInitializeException(String message) {
		super(message);
	}

	public OwlEntityInitializeException(Throwable cause) {
		super(cause);
	}

	private static final long serialVersionUID = -5864201145436154933L;

}
